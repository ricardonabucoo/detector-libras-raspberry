#!/bin/bash

# ü§ñ Gerenciador do Detector LIBRAS - Raspberry Pi 3B+
# Script para facilitar o gerenciamento do sistema

PROJECT_DIR="$HOME/libras_detector"
VENV_DIR="$HOME/libras_detector_env"
SERVICE_NAME="libras-detector"

show_banner() {
    clear
    echo "ü§ñ ==============================================="
    echo "   DETECTOR LIBRAS - RASPBERRY PI 3B+"
    echo "   Gerenciador do Sistema"
    echo "==============================================="
    echo ""
}

check_installation() {
    if [[ ! -d "$PROJECT_DIR" ]] || [[ ! -d "$VENV_DIR" ]]; then
        echo "‚ùå Instala√ß√£o n√£o encontrada!"
        echo "   Execute primeiro: ./setup_raspberry_pi.sh"
        exit 1
    fi
}

show_status() {
    echo "üìä STATUS DO SISTEMA"
    echo "===================="
    
    # Verifica temperatura
    temp=$(vcgencmd measure_temp 2>/dev/null | cut -d'=' -f2)
    echo "üå°Ô∏è  Temperatura: ${temp:-"N/A"}"
    
    # Verifica mem√≥ria
    mem_info=$(free -h | grep Mem | awk '{print $3"/"$2}')
    echo "üß† Mem√≥ria: $mem_info"
    
    # Verifica c√¢mera
    camera_count=$(ls /dev/video* 2>/dev/null | wc -l)
    echo "üì∑ C√¢mera: $camera_count dispositivo(s) detectado(s)"
    
    # Verifica servi√ßo
    if systemctl is-active --quiet $SERVICE_NAME 2>/dev/null; then
        echo "‚öôÔ∏è  Servi√ßo: üü¢ ATIVO"
    else
        echo "‚öôÔ∏è  Servi√ßo: üî¥ INATIVO"
    fi
    
    # Verifica GPIO
    if [[ -d "/sys/class/gpio" ]]; then
        echo "üîå GPIO: ‚úÖ Dispon√≠vel"
    else
        echo "üîå GPIO: ‚ùå Indispon√≠vel"
    fi
    
    echo ""
}

test_camera() {
    echo "üì∑ TESTANDO C√ÇMERA..."
    cd "$PROJECT_DIR"
    source "$VENV_DIR/bin/activate"
    
    if python3 test_camera.py; then
        echo "‚úÖ C√¢mera funcionando corretamente!"
    else
        echo "‚ùå Problema com a c√¢mera detectado"
        echo "üí° Dicas:"
        echo "   - Verifique se a c√¢mera est√° conectada"
        echo "   - Execute: sudo raspi-config ‚Üí Interface Options ‚Üí Camera ‚Üí Enable"
        echo "   - Para c√¢mera USB: ls /dev/video*"
    fi
    
    read -p "Pressione Enter para continuar..."
}

test_gpio() {
    echo "üîå TESTANDO GPIO..."
    cd "$PROJECT_DIR"
    source "$VENV_DIR/bin/activate"
    
    echo "‚ö†Ô∏è  Conecte LEDs nos pinos 18,19,20,21 para ver o teste visual"
    python3 test_gpio.py
    
    read -p "Pressione Enter para continuar..."
}

run_detector() {
    echo "üöÄ INICIANDO DETECTOR..."
    echo "   Ctrl+C para parar"
    echo ""
    
    cd "$PROJECT_DIR"
    source "$VENV_DIR/bin/activate"
    python3 advanced_hand_detection_pi.py
}

install_service() {
    echo "üìã INSTALANDO SERVI√áO SYSTEMD..."
    
    cd "$PROJECT_DIR"
    
    if [[ -f "libras-detector.service" ]]; then
        sudo cp libras-detector.service /etc/systemd/system/
        sudo systemctl daemon-reload
        sudo systemctl enable $SERVICE_NAME
        echo "‚úÖ Servi√ßo instalado com sucesso!"
        echo ""
        echo "Comandos dispon√≠veis:"
        echo "   sudo systemctl start $SERVICE_NAME"
        echo "   sudo systemctl stop $SERVICE_NAME"
        echo "   sudo systemctl status $SERVICE_NAME"
        echo "   sudo journalctl -u $SERVICE_NAME -f"
    else
        echo "‚ùå Arquivo de servi√ßo n√£o encontrado"
        echo "   Execute primeiro a configura√ß√£o completa"
    fi
    
    read -p "Pressione Enter para continuar..."
}

manage_service() {
    if ! systemctl list-units --full -all | grep -Fq "$SERVICE_NAME.service"; then
        echo "‚ùå Servi√ßo n√£o est√° instalado"
        echo "   Use a op√ß√£o 'Instalar Servi√ßo' primeiro"
        read -p "Pressione Enter para continuar..."
        return
    fi
    
    while true; do
        clear
        echo "‚öôÔ∏è  GERENCIAR SERVI√áO"
        echo "==================="
        
        if systemctl is-active --quiet $SERVICE_NAME; then
            echo "Status: üü¢ ATIVO"
        else
            echo "Status: üî¥ INATIVO"
        fi
        
        echo ""
        echo "1) ‚ñ∂Ô∏è  Iniciar servi√ßo"
        echo "2) ‚èπÔ∏è  Parar servi√ßo"
        echo "3) üîÑ Reiniciar servi√ßo"
        echo "4) üìã Ver status detalhado"
        echo "5) üìú Ver logs em tempo real"
        echo "6) üîô Voltar"
        echo ""
        read -p "Escolha uma op√ß√£o: " choice
        
        case $choice in
            1)
                sudo systemctl start $SERVICE_NAME
                echo "‚úÖ Servi√ßo iniciado"
                ;;
            2)
                sudo systemctl stop $SERVICE_NAME
                echo "‚èπÔ∏è  Servi√ßo parado"
                ;;
            3)
                sudo systemctl restart $SERVICE_NAME
                echo "üîÑ Servi√ßo reiniciado"
                ;;
            4)
                sudo systemctl status $SERVICE_NAME
                ;;
            5)
                echo "üìú Logs em tempo real (Ctrl+C para sair):"
                sudo journalctl -u $SERVICE_NAME -f
                ;;
            6)
                break
                ;;
            *)
                echo "‚ùå Op√ß√£o inv√°lida"
                ;;
        esac
        
        if [[ $choice != 5 ]]; then
            read -p "Pressione Enter para continuar..."
        fi
    done
}

configure_detector() {
    echo "‚öôÔ∏è  CONFIGURA√á√ÉO DO DETECTOR"
    echo "============================"
    
    cd "$PROJECT_DIR"
    
    echo "Configura√ß√µes atuais:"
    echo ""
    
    # Mostra configura√ß√µes atuais
    if [[ -f "advanced_hand_detection_pi.py" ]]; then
        target_word=$(grep "self.target_word" advanced_hand_detection_pi.py | cut -d'"' -f2)
        min_area=$(grep "self.min_area" advanced_hand_detection_pi.py | awk '{print $3}')
        max_area=$(grep "self.max_area" advanced_hand_detection_pi.py | awk '{print $3}')
        
        echo "üéØ Palavra alvo: $target_word"
        echo "üìè √Årea m√≠nima da m√£o: $min_area"
        echo "üìê √Årea m√°xima da m√£o: $max_area"
        echo ""
        
        echo "Op√ß√µes:"
        echo "1) Alterar palavra alvo"
        echo "2) Ajustar sensibilidade da detec√ß√£o"
        echo "3) Alterar pinos GPIO do motor"
        echo "4) Voltar"
        echo ""
        
        read -p "Escolha uma op√ß√£o: " config_choice
        
        case $config_choice in
            1)
                read -p "Digite a nova palavra alvo (apenas letras): " new_word
                new_word=$(echo "$new_word" | tr '[:lower:]' '[:upper:]' | tr -cd '[:alpha:]')
                if [[ -n "$new_word" ]]; then
                    sed -i "s/self.target_word = \".*\"/self.target_word = \"$new_word\"/" advanced_hand_detection_pi.py
                    echo "‚úÖ Palavra alvo alterada para: $new_word"
                fi
                ;;
            2)
                echo "Ajustar √°rea de detec√ß√£o da m√£o:"
                echo "Valores menores = mais sens√≠vel (detecta m√£os menores/distantes)"
                echo "Valores maiores = menos sens√≠vel (apenas m√£os grandes/pr√≥ximas)"
                echo ""
                read -p "Nova √°rea m√≠nima (atual: $min_area): " new_min
                read -p "Nova √°rea m√°xima (atual: $max_area): " new_max
                
                if [[ "$new_min" =~ ^[0-9]+$ ]] && [[ "$new_max" =~ ^[0-9]+$ ]]; then
                    sed -i "s/self.min_area = .*/self.min_area = $new_min/" advanced_hand_detection_pi.py
                    sed -i "s/self.max_area = .*/self.max_area = $new_max/" advanced_hand_detection_pi.py
                    echo "‚úÖ Configura√ß√£o de √°rea alterada"
                fi
                ;;
            3)
                current_pins=$(grep "self.motor_pins = " advanced_hand_detection_pi.py | cut -d'[' -f2 | cut -d']' -f1)
                echo "Pinos GPIO atuais: [$current_pins]"
                echo "Digite 4 n√∫meros de pinos GPIO separados por espa√ßo (ex: 17 18 27 22):"
                read -p "Novos pinos: " new_pins
                
                if [[ "$new_pins" =~ ^[0-9\ ]+$ ]]; then
                    formatted_pins=$(echo "$new_pins" | tr ' ' ', ')
                    sed -i "s/self.motor_pins = \[.*\]/self.motor_pins = [$formatted_pins]/" advanced_hand_detection_pi.py
                    echo "‚úÖ Pinos GPIO alterados para: [$formatted_pins]"
                fi
                ;;
        esac
    else
        echo "‚ùå Arquivo principal n√£o encontrado"
    fi
    
    read -p "Pressione Enter para continuar..."
}

backup_system() {
    echo "üíæ BACKUP DO SISTEMA"
    echo "===================="
    
    backup_name="libras_detector_backup_$(date +%Y%m%d_%H%M%S).tar.gz"
    
    echo "Criando backup: $backup_name"
    tar -czf "/tmp/$backup_name" "$PROJECT_DIR" "$VENV_DIR" 2>/dev/null
    
    if [[ $? -eq 0 ]]; then
        echo "‚úÖ Backup criado em: /tmp/$backup_name"
        echo "üí° Para restaurar: tar -xzf /tmp/$backup_name -C ~/"
    else
        echo "‚ùå Erro ao criar backup"
    fi
    
    read -p "Pressione Enter para continuar..."
}

view_logs() {
    echo "üìú LOGS DO SISTEMA"
    echo "=================="
    echo ""
    echo "1) Logs do servi√ßo systemd"
    echo "2) Logs de detec√ß√£o (se existir)"
    echo "3) Logs do sistema (dmesg)"
    echo "4) Voltar"
    echo ""
    
    read -p "Escolha uma op√ß√£o: " log_choice
    
    case $log_choice in
        1)
            if systemctl list-units --full -all | grep -Fq "$SERVICE_NAME.service"; then
                echo "üìú Logs do servi√ßo (Ctrl+C para sair):"
                sudo journalctl -u $SERVICE_NAME -f
            else
                echo "‚ùå Servi√ßo n√£o instalado"
            fi
            ;;
        2)
            if [[ -f "$PROJECT_DIR/detector.log" ]]; then
                echo "üìú √öltimas 50 linhas do log de detec√ß√£o:"
                tail -50 "$PROJECT_DIR/detector.log"
            else
                echo "‚ùå Log de detec√ß√£o n√£o encontrado"
            fi
            read -p "Pressione Enter para continuar..."
            ;;
        3)
            echo "üìú Logs do sistema (√∫ltimas 50 linhas):"
            dmesg | tail -50
            read -p "Pressione Enter para continuar..."
            ;;
        4)
            return
            ;;
    esac
}

main_menu() {
    while true; do
        show_banner
        check_installation
        show_status
        
        echo "MENU PRINCIPAL"
        echo "=============="
        echo "1) üöÄ Executar Detector"
        echo "2) üì∑ Testar C√¢mera"
        echo "3) üîå Testar GPIO/Motor"
        echo "4) üìã Instalar Servi√ßo"
        echo "5) ‚öôÔ∏è  Gerenciar Servi√ßo"
        echo "6) üîß Configurar Detector"
        echo "7) üìú Ver Logs"
        echo "8) üíæ Backup do Sistema"
        echo "9) ‚ùå Sair"
        echo ""
        
        read -p "Escolha uma op√ß√£o (1-9): " choice
        
        case $choice in
            1) run_detector ;;
            2) test_camera ;;
            3) test_gpio ;;
            4) install_service ;;
            5) manage_service ;;
            6) configure_detector ;;
            7) view_logs ;;
            8) backup_system ;;
            9) 
                echo "üëã At√© logo!"
                exit 0
                ;;
            *)
                echo "‚ùå Op√ß√£o inv√°lida. Pressione Enter para tentar novamente."
                read
                ;;
        esac
    done
}

# Verifica se est√° sendo executado como root
if [[ $EUID -eq 0 ]]; then
    echo "‚ùå N√£o execute este script como root (sudo)"
    echo "   Execute como usu√°rio normal: ./libras_manager.sh"
    exit 1
